#!/bin/sh

# =========================
# –Э–∞—Б—В—А–Њ–є–Ї–Є
# =========================
API="https://api.openweathermap.org/data/2.5/weather"
KEY="e434b5435a979de6e155570590bee89b"
CITY="Novosibirsk"
UNITS="metric"
SYMBOL="¬∞"
TIMEOUT=5

# =========================
# –Т—Л–±–Њ—А –Є–Ї–Њ–љ–Ї–Є (Nerd Font)
# =========================
get_icon() {
    case "$1" in
        01d) echo "пЖЕ" ;;    # —П—Б–љ–Њ, –і–µ–љ—М
        01n) echo "пЖЖ" ;;    # —П—Б–љ–Њ, –љ–Њ—З—М
        02d) echo "оМВ" ;;    # –Љ–∞–ї–Њ–Њ–±–ї–∞—З–љ–Њ, –і–µ–љ—М
        02n) echo "оМГ" ;;    # –Љ–∞–ї–Њ–Њ–±–ї–∞—З–љ–Њ, –љ–Њ—З—М
        03*|04*) echo "пГВ" ;; # –Њ–±–ї–∞—З–љ–Њ
        09*) echo "оМШ" ;;    # –ї–Є–≤–µ–љ—М
        10d) echo "оМИ" ;;    # –і–Њ–ґ–і—М, –і–µ–љ—М
        10n) echo "оМЙ" ;;    # –і–Њ–ґ–і—М, –љ–Њ—З—М
        11*) echo "оМП" ;;    # –≥—А–Њ–Ј–∞
        13*) echo "пЛЬ" ;;    # —Б–љ–µ–≥
        50*) echo "оНЊ" ;;    # —В—Г–Љ–∞–љ
        *) echo "пБЩ" ;;      # –љ–µ–Є–Ј–≤–µ—Б—В–љ–Њ
    esac
}

# =========================
# –Я–Њ–ї—Г—З–µ–љ–Є–µ –і–∞–љ–љ—Л—Е
# =========================
weather=$(curl -sf --max-time "$TIMEOUT" \
    "$API?appid=$KEY&q=$CITY&units=$UNITS")

if [ -z "$weather" ]; then
    printf '{"text":"пБ±  --","tooltip":"–Э–µ —Г–і–∞–ї–Њ—Б—М –њ–Њ–ї—Г—З–Є—В—М –і–∞–љ–љ—Л–µ"}\n'
    exit 1
fi

# =========================
# –Ш–Ј–≤–ї–µ—З–µ–љ–Є–µ –і–∞–љ–љ—Л—Е
# =========================
temp=$(jq '.main.temp | floor // empty' <<<"$weather")
feels_like=$(jq '.main.feels_like | floor // empty' <<<"$weather")
icon_code=$(jq -r '.weather[0].icon // "na"' <<<"$weather")

if [ -z "$temp" ] || [ -z "$feels_like" ]; then
    printf '{"text":"пБ±  --","tooltip":"–Э–µ—В –і–∞–љ–љ—Л—Е"}\n'
    exit 1
fi

icon=$(get_icon "$icon_code")

# =========================
# Tooltip
# =========================
tooltip="–Ю—Й—Г—Й–∞–µ—В—Б—П –Ї–∞–Ї ${feels_like}${SYMBOL}"

# =========================
# –Т—Л–≤–Њ–і –і–ї—П Waybar
# =========================
# %-4s вАФ —Д–Є–Ї—Б–Є—А–Њ–≤–∞–љ–љ–∞—П —И–Є—А–Є–љ–∞ –Є–Ї–Њ–љ–Ї–Є
printf '{"text":"%-4s  %s%s","tooltip":"%s"}\n' \
    "$icon" "$temp" "$SYMBOL" "$tooltip"
